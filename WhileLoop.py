# While loop
cities = ['New York', 'Washington', 'Los Angles', 'San Francisco', 'Chicago', 'Florida', 'Texas']
#print(cities)
number = len(cities)
#print(number)
number = number - 1
#print(number)
round = 0
#print(round)
while round <= number:
 print(cities[round])
 #print(round)
 round = round + 1

print('End of the loop')